// -------------------------------------------------------- ARITHMETIC -------------------------------------------------------
var a = 4;
var b = 2;

var plus = a + b;
var minus = a - b;
var multiply = a * b;
var division = a / b;
var exponent = a ** b; // means 4 * 4 OR 4 power of 2
var modulo = a % b; // remainder of division

console.log("Plus =", plus);
console.log("Minus =", minus);
console.log("Multiply =", multiply);
console.log("Division =", division);
console.log("Exponent =", exponent);
console.log("Modulo =", modulo);
